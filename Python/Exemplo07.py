# Indentação
# é uma forma de arruma o codigo, fazendo com que algumas linhas fiquem mais a direitaque outra, a medida que adicionamos espaços em seu inicio. Enquanto na maioria das linguagens, como C, Java e PHP, os blocos de código são delimitados por chaves ({ }) ou comandos, em Python, os blocos são delimitados por espaços ou tabulações, formando uma indentação visual.
# Não existem símbolos de “abre” e “fecha”.
# exemplos de identitação:

idade = int (input('Digite a idade da pessoa:'))
if idade >= 18:
    print('Maior idade')
else: 
    print("Menor idade")