n1 = int(input('Digite um numero: '))
a = n1 - 1
s = n1 + 1
d = n1 ** 2
t = n1 ** 3
r = n1 ** (1/2)
print('O numero digitado é: {}\n seu sucessor é :{}\n seu antecessor é: {}!'.format(n1,s,a))

print('O Dobro de {} é: {}'.format(n1,d))
print('O Triplo de {} é: {}'.format(n1,t))
print('A Raiz quadrada de {} é: {}'.format(n1,r))
print('A Raiz cubica de {} é: {}'.format(n1,n1**(1/3)))
